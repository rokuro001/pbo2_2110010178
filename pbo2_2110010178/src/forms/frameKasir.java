    public void tampilDataKasir(){
        try {
            modelTableKasir.getDataVector().removeAllElements();
            modelTableKasir.fireTableDataChanged();
            
            PreparedStatement query = koneksi.connect.prepareStatement("SELECT * FROM kasir");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] baris = new Object[3];
                baris[0] = data.getString("id_kasir");
                baris[1] = data.getString("nama_admin");
                baris[2] = data.getString("no_telp");
                modelTableKasir.addRow(baris);
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }


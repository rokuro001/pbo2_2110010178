    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
        try {
            if (
                    (txtidkasir.getText().isEmpty()) &&
                    (txtnama.getText().isEmpty()) &&
                    (txttelepon.getText().isEmpty())
               ){
                JOptionPane.showMessageDialog(this, "Silahkan Pilih Data Terlebih Dahulu");
            }else
            {
                String sql = "UPDATE kasir Set nama_admin=?,no_telp=? WHERE id_kurir=?";
                PreparedStatement ubah = koneksi.connect.prepareStatement(sql);
                ubah.setString(1, txtnama.getText());
                ubah.setString(2, txttelepon.getText());
                ubah.setString(3, txtidkasir.getText());
                ubah.executeUpdate();
                tampilDataKasir();
                JOptionPane.showMessageDialog(this, "Data Telah Diubah");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnubahActionPerformed


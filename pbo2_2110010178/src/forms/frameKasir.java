    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
           try {
            if(txtidkasir.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "id belum diisi");
               txtidkasir.requestFocus();
            } else if(txtnama.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "nama admin belum diisi");
               txtnama.requestFocus();
            } else if(txttelepon.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "no telepon belum diisi");
               txttelepon.requestFocus();
            }else{
                String sql = "insert into kasir value('"+txtidkasir.getText()+"','"+txtnama.getText()+"','"+txttelepon.getText()+"')";
                koneksi.connect.createStatement().execute(sql);
                tampilDataKasir();
                JOptionPane.showMessageDialog(this, "data berhasil disimpan");
            }
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnsimpanprepareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanprepareActionPerformed
        // TODO add your handling code here:
      
           try {
            if(txtidkasir.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "id belum diisi");
               txtidkasir.requestFocus();
            } else if(txtnama.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "nama admin belum diisi");
               txtnama.requestFocus();
            } else if(txttelepon.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "no telepon belum diisi");
               txttelepon.requestFocus();
            } else{
                String sqlCari = "SELECT * FROM kasir WHERE id_kasir ='"+txtidkasir.getText()+"'";
               ResultSet cari = koneksi.connect.createStatement().executeQuery(sqlCari);
               if (cari.next()){
                    JOptionPane.showMessageDialog(this, "ID sudah terdaftar");
                                        
                       txtnama.setText(cari.getString(2));
                       txttelepon.setText(cari.getString(3));                   
               }else{
                    String sql = "INSERT INTO kasir VALUE(?,?,?)";
                    PreparedStatement simpan = koneksi.connect.prepareStatement(sql);
                    simpan.setString(1, txtidkasir.getText());
                    simpan.setString(2, txtnama.getText());
                    simpan.setString(3, txttelepon.getText());
                    simpan.executeUpdate();
                    tampilDataKasir();
                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
               }
               
           }  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnsimpanprepareActionPerformed


    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
        try {
            if (
                    (txtidpaket.getText().isEmpty()) &&
                    (txtjenis.getText().isEmpty()) &&
                    (txtharga.getText().isEmpty()) &&
                    (txtantar.getText().isEmpty()) 
               ){
                JOptionPane.showMessageDialog(this, "Silahkan Pilih Data Terlebih Dahulu");
            }else
            {
                String sql = "UPDATE paket_laundry Set jenis_paket=?,harga_paket=?,antar_jemput=? WHERE id_paket=?";
                PreparedStatement ubah = koneksi.connect.prepareStatement(sql);
                ubah.setString(1, txtjenis.getText());
                ubah.setString(2, txtharga.getText());
                ubah.setString(3, txtantar.getText());
                ubah.setString(4, txtidpaket.getText());                
                ubah.executeUpdate();
                tampilDataPaket();
                JOptionPane.showMessageDialog(this, "Data Telah Diubah");
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnubahActionPerformed


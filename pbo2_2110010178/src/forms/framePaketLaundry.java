    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
         try {
            if(txtidpaket.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "id belum diisi");
               txtidpaket.requestFocus();
            } else if(txtjenis.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "jenis paket belum diisi");
               txtjenis.requestFocus();
            } else if(txtharga.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "harga belum diisi");
               txtharga.requestFocus();
            } else if(txtantar.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "antar jemput belum diisi");
               txtantar.requestFocus();   
            }else{
                String sql = "insert into paket_laundry value('"+txtidpaket.getText()+"','"+txtjenis.getText()+"','"+txtharga.getText()+"','"+txtantar.getText()+"')";
                koneksi.connect.createStatement().execute(sql);
                tampilDataPaket();
                JOptionPane.showMessageDialog(this, "data berhasil disimpan");
            }
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnSimpanPrepareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanPrepareActionPerformed
        // TODO add your handling code here:
        try {
            if(txtidpaket.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "id belum diisi");
               txtidpaket.requestFocus();
            } else if(txtjenis.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Jenis Paket belum diisi");
               txtjenis.requestFocus();
            } else if(txtharga.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Harga Paket belum diisi");
               txtharga.requestFocus();
            } else if(txtantar.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Antar Jemput belum diisi");
               txtantar.requestFocus();
            } else{
               String sqlCari = "SELECT * FROM paket_laundry WHERE id_paket ='"+txtidpaket.getText()+"'";
               ResultSet cari = koneksi.connect.createStatement().executeQuery(sqlCari);
               if (cari.next()){
                    JOptionPane.showMessageDialog(this, "ID sudah terdaftar");
                                        
                       txtjenis.setText(cari.getString(2));
                       txtharga.setText(cari.getString(3));                   
                       txtantar.setText(cari.getString(4));                   
               }else{
                    String sql = "INSERT INTO paket_laundry VALUE(?,?,?,?)";
                    PreparedStatement simpan = koneksi.connect.prepareStatement(sql);
                    simpan.setString(1, txtidpaket.getText());
                    simpan.setString(2, txtjenis.getText());
                    simpan.setString(3, txtharga.getText());
                    simpan.setString(4, txtantar.getText());
                    simpan.executeUpdate();
                    tampilDataPaket();
                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
               }
               
           }  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnSimpanPrepareActionPerformed


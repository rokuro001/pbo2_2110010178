     public void tampilDataPaket(){
        try {
            modelTablePaket.getDataVector().removeAllElements();
            modelTablePaket.fireTableDataChanged();
            
            PreparedStatement query = koneksi.connect.prepareStatement("SELECT * FROM paket_laundry");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] baris = new Object[4];
                baris[0] = data.getString("id_paket");
                baris[1] = data.getString("jenis_paket");
                baris[2] = data.getString("harga_paket");
                baris[3] = data.getString("antar_jemput");
                modelTablePaket.addRow(baris);
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }
